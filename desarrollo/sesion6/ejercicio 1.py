# Definimos un conjunto llamado 's' a partir de una lista con elementos duplicados.
# Los conjuntos automáticamente eliminan los elementos duplicados y no mantienen un orden específico.
s = set ([5,4 ,6,8,8,1])

# Imprimimos el conjunto resultante (los duplicados han sido eliminados y el orden puede variar).
print(s)
# Imprimimos el tipo de dato de 's', que es 'set'.
print(type(s))